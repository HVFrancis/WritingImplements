
/**
 * An Interface used to give erasable properties to all types of utensils beyond just pencils.
 *
 * @author Raymond Terry
 * @version 1
 */
public interface Erasable
{
    /**
     * Method to describe the writing implement erasing something.
     *
     * @param   something   something being erased.
     */
    public void erase(String something);
}

